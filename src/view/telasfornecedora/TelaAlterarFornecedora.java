    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        DaoDistribuidora dd=  new DaoDistribuidora();
        String nome = txtNomeDistribuidora.getText();
        int cnpj = Integer.parseInt(txtCnpj.getText());
        String endereco = txtEndereco.getText();
        if(nome != "" && cnpj != 0 && endereco != ""){
            int row = tabelaDistribuidora.getSelectedRow();
            Distribuidora distribuidora=  dd.busca(Integer.parseInt(tabelaDistribuidora.getValueAt(row, 0).toString()));
            distribuidora.setNome(nome);
            distribuidora.setCnpj(cnpj);
            distribuidora.setEndereco(endereco);
            dd.update(distribuidora);
        }
        else{
            JOptionPane.showMessageDialog(null, "Os campos devem estar preenchidos!");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

