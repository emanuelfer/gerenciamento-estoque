    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        if(!txtId.getText().trim().isEmpty()){
            int id = Integer.parseInt(txtId.getText());
            DefaultTableModel ts = (DefaultTableModel)tabelaSaida.getModel();
            ts.setNumRows(0);
            DaoSaida ds = new DaoSaida();
            Saida saida = new Saida();
            saida = ds.busca(id);
            System.out.println(saida.getLote().getId());
            if(saida == null){
                JOptionPane.showMessageDialog(null, "Nenhum resultado encontrado!");
            }else{
                Object[] dados = {saida.getId(), saida.getLote().getId(), saida.getDistribuidora().getNome(), saida.getGerente().getNome(), saida.getDataDeSaida().toString()};
                ts.addRow(dados);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Digite um ID!");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnMostarTodosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostarTodosActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tc = (DefaultTableModel)tabelaSaida.getModel();
        tc.setNumRows(0);
        DaoSaida ds = new DaoSaida();
        ArrayList<Saida> saida = new ArrayList<Saida>();
        saida = ds.mostraSaidas();
        if(saida.size() == 0){
            JOptionPane.showMessageDialog(null, "Nenhum resultado encontrado!");
        }else{
            for(int i =0; i<saida.size(); i++) {
                Object[] dados = {saida.get(i).getId(), saida.get(i).getLote().getId(), saida.get(i).getDistribuidora().getNome(), saida.get(i).getGerente().getNome(), saida.get(i).getDataDeSaida().toString()};
                tc.addRow(dados);
            }
        }
    }//GEN-LAST:event_btnMostarTodosActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        String dataSaida = txtDataSaida.getText();
        
        DaoGerente dg = new DaoGerente();
        ArrayList<Gerente> gerente = dg.buscaNome(cbGerente.getSelectedItem().toString());
        
        DaoDistribuidora dd = new DaoDistribuidora();
        ArrayList<Distribuidora> distribuidora = dd.buscaNome(cbDistribuidora.getSelectedItem().toString());
       
        DaoLote dl = new DaoLote();
        Lote lote = dl.busca(Integer.parseInt(cbLote.getSelectedItem().toString()));
        lote.setDisponivel(false);
        dl.update(lote);
        if(!txtDataSaida.getText().trim().isEmpty()){
            DaoSaida ds = new DaoSaida();
            int row = tabelaSaida.getSelectedRow();
            Saida saida = ds.busca(Integer.parseInt(tabelaSaida.getValueAt(row, 0).toString()));
            saida.setDataDeSaida(dataSaida);
            saida.setDistribuidora(distribuidora.get(0));
            saida.setGerente(gerente.get(0));
            saida.setLote(lote);
            ds.update(saida);
        }else{
            JOptionPane.showMessageDialog(null, "Preencha todos os campos");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

