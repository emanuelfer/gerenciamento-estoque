    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        String nome = txtNome.getText();
        String marca = txtMarca.getText();
        double peso = Double.parseDouble(txtPeso.getText());
        double preco = Double.parseDouble(txtPreco.getText());
        DaoCategoria dc = new DaoCategoria();
        ArrayList<Categoria> categoria = dc.buscaNome(cbCategoria.getSelectedItem().toString());
        if(nome != "" && marca != "" && peso != 0 && preco != 0.0 && categoria != null){
            Produto produto = new Produto(categoria.get(0), marca, peso, nome, preco);
            DaoProduto dp = new DaoProduto();
            dp.create(produto);
        }else{
            JOptionPane.showMessageDialog(null, "Preencha todos os campos");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

