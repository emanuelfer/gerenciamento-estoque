    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:

        if(!txtId.getText().trim().isEmpty()){

            int id = Integer.parseInt(txtId.getText());

            DefaultTableModel tl = (DefaultTableModel)tabelaLote.getModel();
            tl.setNumRows(0);
            DaoLote dl = new DaoLote();
            Lote lote = new Lote();
            lote = dl.busca(id);

            if(lote == null){
                JOptionPane.showMessageDialog(null, "Nenhum resultado encontrado!");
            }else{
                Object[] dados = {lote.getId(), lote.getGerente().getNome(), lote.getFornecedora().getNome(), lote.getProduto().getNome(), lote.getQtdProduto(), lote.getValidade().toString(), lote.getDataDeEntrada().toString()};
                tl.addRow(dados);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Insira um ID!");
        }

    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnMostrarTodosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarTodosActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tl = (DefaultTableModel)tabelaLote.getModel();
        tl.setNumRows(0);
        DaoLote dl = new DaoLote();
        ArrayList<Lote> lote = new ArrayList<Lote>();
        lote = dl.mostraLotes();
        if(lote.size() == 0){
            JOptionPane.showMessageDialog(null, "Nenhum resultado encontrado!");
        }else{
            for(int i =0; i<lote.size(); i++) {
                Object[] dados = {lote.get(i).getId(), lote.get(i).getGerente().getNome(), lote.get(i).getFornecedora().getNome(), lote.get(i).getProduto().getNome(), lote.get(i).getQtdProduto(), lote.get(i).getValidade().toString(), lote.get(i).getDataDeEntrada().toString()};
                tl.addRow(dados);
            }
        }
    }//GEN-LAST:event_btnMostrarTodosActionPerformed

