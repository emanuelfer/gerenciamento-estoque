    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        DaoLote dl = new DaoLote();
        String gerente = cbGerente.getSelectedItem().toString();
        String fornecedora = cbFornecedora.getSelectedItem().toString();
        String produto = cbProduto.getSelectedItem().toString();
        int qtdProduto = Integer.parseInt(txtQtdProduto.getText());
        String validade = txtValidade.getText();
        String dataDeEntrada = txtDataDeEntrada.getText();
        
        Date dataValidade = null;
        Date dataEntrada = null;
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        try {
            dataValidade = formato.parse(validade);
            dataEntrada = formato.parse(dataDeEntrada);
        } catch (ParseException ex) {
            Logger.getLogger(TelaCadastrarLote.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        long dt = ((dataValidade.getTime() - dataEntrada.getTime()) + 3600000 )/ 86400000L;

        
        if(dt>0 && !txtQtdProduto.getText().trim().isEmpty() && !txtValidade.getText().trim().isEmpty() && !txtDataDeEntrada.getText().trim().isEmpty()){
            int row = tabelaLote.getSelectedRow();
            Lote lote = null;
            lote = dl.busca(Integer.parseInt(tabelaLote.getValueAt(row, 0).toString()));
            lote.setDataDeEntrada(dataDeEntrada);
            lote.setValidade(validade);
            lote.setQtdProduto(qtdProduto);
                
            DaoGerente dc = new DaoGerente();
            ArrayList<Gerente> gerentes = dc.buscaNome(cbGerente.getSelectedItem().toString());

            DaoFornecedora df = new DaoFornecedora();
            ArrayList<Fornecedora> fornecedoras = df.buscaNome(cbFornecedora.getSelectedItem().toString());

            DaoProduto dp = new DaoProduto();
            ArrayList<Produto> produtos = dp.buscaNome(cbProduto.getSelectedItem().toString());
                
            lote.setFornecedora(fornecedoras.get(0));
            lote.setGerente(gerentes.get(0));
            System.out.println(lote.getGerente().getId());
            lote.setProduto(produtos.get(0));
                
            
            dl.update(lote);
        }
        else{
            JOptionPane.showMessageDialog(null, "Os campos devem estar preenchidos corretamente!");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed


