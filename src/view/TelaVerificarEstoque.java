    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tp = (DefaultTableModel)tabelaProduto.getModel();
        tp.setNumRows(0);
        String nome = txtNome.getText();
        DaoProduto dp = new DaoProduto();
        ArrayList<Produto> produtos = new ArrayList<Produto>();
        produtos = dp.buscaNome(nome);
        if(produtos.size() == 0){
            JOptionPane.showMessageDialog(null, "Nenhum produto encontrado!");
        }else{
            for(int i = 0; i < produtos.size(); i++){
                Object[] dados = {produtos.get(i).getId(), produtos.get(i).getNome(), produtos.get(i).getMarca(), produtos.get(i).getCaegoria().getNome(), produtos.get(i).getPeso(), produtos.get(i).getPreco()};
                tp.addRow(dados);
            }
        }

    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnQuantidadeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQuantidadeActionPerformed
        // TODO add your handling code here:
        int row = tabelaProduto.getSelectedRow();
        int id = Integer.parseInt(tabelaProduto.getValueAt(row, 0).toString());
        DaoLote dl = new DaoLote();
        int quantidade = dl.calcularQuantidade(id);
        JOptionPane.showMessageDialog(null, "Quantidade de produtos: " + quantidade);
    }//GEN-LAST:event_btnQuantidadeActionPerformed


